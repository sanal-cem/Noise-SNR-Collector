CREATE TABLE IF NOT EXISTS WSNR (
  ID INT(8) NOT NULL AUTO_INCREMENT PRIMARY KEY,
  ROUTENUM INT(8),
  SNR DOUBLE(10,3),
  LOC_X DOUBLE(10,8),
  LOC_Y DOUBLE(10,8),
  SDATE DATE DEFAULT NULL,
  STIME TIME
) ENGINE=InnoDB

